<?php
$celciusss = 37.841;
$reamurrr = (4/5) * $celciusss;
$fahrenheittt = (9/5) * $celciusss + 32;
$kelvinnn = $celciusss + 273.15;
echo "Fahrenheit (F) = " . number_format($fahrenheittt, 4, ',', '.') . "<br>";
echo "Reamur (R) = " . number_format($reamurrr, 4, ',', '.') . "<br>";
echo "Kelvin (K) = " . number_format($kelvinnn, 3, ',', '.') . "<br>";
?>